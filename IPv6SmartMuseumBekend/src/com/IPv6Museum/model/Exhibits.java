package com.IPv6Museum.model;

public class Exhibits {
	private String Exhibits_id;
	private String Exhibits_name;
	private String Exhibits_img;
	public String getExhibits_id() {
		return Exhibits_id;
	}
	public void setExhibits_id(String exhibits_id) {
		Exhibits_id = exhibits_id;
	}
	public String getExhibits_name() {
		return Exhibits_name;
	}
	public void setExhibits_name(String exhibits_name) {
		Exhibits_name = exhibits_name;
	}
	public String getExhibits_img() {
		return Exhibits_img;
	}
	public void setExhibits_img(String exhibits_img) {
		Exhibits_img = exhibits_img;
	}

}
